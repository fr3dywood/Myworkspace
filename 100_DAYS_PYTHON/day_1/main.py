# Affiche un message de bienvenue pour le générateur de nom de groupe
print("Bienvenue dans le Générateur de Nom de Groupe.")

# Demande à l'utilisateur le nom de la ville où il a grandi
ville = input("Quel est le nom de la ville où vous avez grandi?")

# Demande à l'utilisateur le nom de son animal de compagnie
animal = input("Quel est le nom de votre animal de compagnie?")

# Combine les deux réponses pour générer un nom de groupe et l'affiche
print("Le nom de votre groupe pourrait être " + ville + " " + animal)